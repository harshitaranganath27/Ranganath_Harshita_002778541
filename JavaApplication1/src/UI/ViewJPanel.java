    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        String txt = txtSearch.getText();
        if (txt.equals("")) {
            JOptionPane.showMessageDialog(this, "Please enter string to search");
            populateTable();
        } else {
            DefaultTableModel model = (DefaultTableModel) tblEmployee.getModel();
            TableRowSorter<DefaultTableModel> row = new TableRowSorter<>(model);
            tblEmployee.setRowSorter(row);
            row.setRowFilter(RowFilter.regexFilter(txt.trim()));
        }

    }//GEN-LAST:event_btnSearchActionPerformed

