    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblEmployee.getModel();  // (DefaultTableModel)-> typecasting the table model being returned
        model.setRowCount(0);
        for (Employee em : empDetails.getHistory()) {    //em traverses through ebery item in the empDetails
            Object[] row = new Object[10];
            row[0] = em;
            row[1] = em.getName();
            row[2] = em.getAge();
            row[3] = em.getGender();
            row[4] = em.getStartDate();
            row[5] = em.getPositionTitle();
            row[6] = em.getTeamInfo();
            row[7] = em.getPhoneNumber();
            row[8] = em.getEmailId();
            row[9] = em.getLevel();
             model.addRow(row);
        }

    }

