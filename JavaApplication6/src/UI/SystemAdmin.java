    private Boolean checkBlankInput() {
        if (txtuname.getText().length() == 0 || txtpassword.getPassword().length == 0 || (chkbxHospAdmin.getText().length() == 0 && chkbxPatient.getText().length() == 0 && chkbxDoctor.getText().length() == 0 && chkbxCommAdmin.getText().length() == 0)) {
            return false;
        } else {
            return true;
        }
    }

    private boolean checkbox() {
        JCheckBox[] newList = new JCheckBox[4];
        newList[0] = chkbxCommAdmin;
        newList[1] = chkbxHospAdmin;
        newList[2] = chkbxDoctor;
        newList[3] = chkbxPatient;
        int k = 0;
        for (int i = 0; i < newList.length; i++) {
            if (newList[i].isSelected()) {
                k += 1;
            }
        }
        if (k == 1) {
            return false;
        } else {
            return true;
        }
    }

